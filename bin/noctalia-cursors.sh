#!/bin/bash
# =============================================================================
# noctalia-cursors.sh — Oreo cursor recolouring + hyprcursor conversion module
#
# Flow:
#   1. Check deps (ruby, bash, hyprcursor-util, hyprctl)
#   2. Check hyprcursor cache → apply instantly if hit
#   3. Cache miss:
#      a. Keep current cursor active
#      b. Generate SVGs with ruby generator/convert.rb
#      c. Build XCursor with bash build.sh (needed for GTK fallback)
#      d. Build hyprcursor working-state from SVGs
#      e. Compile with hyprcursor-util --create
#      f. Cache both themes
#      g. Apply hyprcursor theme
#
# Cache locations:
#   Hyprcursor: ~/.local/share/icons/theme_oreo_noctalia_PRIMARY_hyprcursor/
#   XCursor:    ~/.local/share/icons/oreo_noctalia_PRIMARY_cursors/
#               (also symlinked to ~/.icons/ for GTK compatibility)
#
# Note: hyprcursor-util --create prefixes output directory with "theme_"
#
# Expects: PRIMARY exported by noctalia-apply.sh
# =============================================================================

MODULE="cursors"
CURSOR_SIZE=28
OREO_DIR="$HOME/oreo-cursors"
ICONS_DIR="$HOME/.local/share/icons"
XCURSOR_COMPAT_DIR="$HOME/.icons"   # GTK fallback location

THEME_NAME="oreo_noctalia_${PRIMARY}"
HYPR_THEME_NAME="${THEME_NAME}_hyprcursor"
HYPR_COMPILED_NAME="theme_${HYPR_THEME_NAME}"   # hyprcursor-util prefixes output with "theme_"
XCURSOR_THEME_NAME="${THEME_NAME}_cursors"

HYPR_CACHE="$ICONS_DIR/$HYPR_COMPILED_NAME"     # actual on-disk location
XCURSOR_CACHE="$ICONS_DIR/$XCURSOR_THEME_NAME"
XCURSOR_COMPAT_LINK="$XCURSOR_COMPAT_DIR/$XCURSOR_THEME_NAME"

# --- Dependency checks -------------------------------------------------------
if ! command -v ruby &>/dev/null; then
    echo "[${MODULE}] skipped — ruby not found. Install with: sudo pacman -S ruby"
    exit 0
fi

if ! command -v hyprcursor-util &>/dev/null; then
    echo "[${MODULE}] skipped — hyprcursor-util not found. Install with: sudo pacman -S hyprcursor"
    exit 0
fi

if ! command -v hyprctl &>/dev/null; then
    echo "[${MODULE}] skipped — hyprctl not found. Is Hyprland running?"
    exit 0
fi

if [ ! -d "$OREO_DIR" ]; then
    echo "[${MODULE}] skipped — oreo-cursors repo not found at $OREO_DIR"
    echo "[${MODULE}]   Clone it with: git clone https://github.com/nicehash/oreo-cursors ~/oreo-cursors"
    exit 0
fi

if [ ! -f "$OREO_DIR/generator/convert.rb" ]; then
    echo "[${MODULE}] skipped — convert.rb not found in $OREO_DIR/generator/"
    exit 0
fi

if [ ! -f "$OREO_DIR/build.sh" ]; then
    echo "[${MODULE}] skipped — build.sh not found in $OREO_DIR"
    exit 0
fi

# Ensure compat dir exists
mkdir -p "$XCURSOR_COMPAT_DIR"

# --- Helper: set GTK/XCursor fallback ----------------------------------------
_apply_xcursor_fallback() {
    if [ -d "$XCURSOR_CACHE" ]; then
        ln -sfn "$XCURSOR_CACHE" "$XCURSOR_COMPAT_LINK"
        gsettings set org.gnome.desktop.interface cursor-theme "$XCURSOR_THEME_NAME" 2>/dev/null \
            || dconf write /org/gnome/desktop/interface/cursor-theme \
               "'${XCURSOR_THEME_NAME}'" 2>/dev/null
        echo "[${MODULE}] XCursor fallback set for GTK apps."
    fi
}

# --- Cache hit — apply immediately and exit ----------------------------------
if [ -d "$HYPR_CACHE" ]; then
    echo "[${MODULE}] Cache hit — applying #${PRIMARY} hyprcursor theme instantly"
    hyprctl setcursor "$HYPR_COMPILED_NAME" "$CURSOR_SIZE"
    _apply_xcursor_fallback
    exit 0
fi

# --- Cache miss — build from scratch -----------------------------------------
echo "[${MODULE}] No cache found for #${PRIMARY} — building now (this takes ~20s)..."
echo "[${MODULE}] Current cursor will remain active until build completes."

# --- Step 1: Write colours.conf for Oreo generator ---------------------------
COLOURS_CONF="$OREO_DIR/generator/colours.conf"
cat > "$COLOURS_CONF" <<EOF
# Generated by noctalia-cursors.sh
noctalia_${PRIMARY} = #${PRIMARY} Label Shadow 0.6
EOF

# --- Step 2: Generate SVGs ---------------------------------------------------
echo "[${MODULE}] Generating SVGs..."
cd "$OREO_DIR" || exit 1

if ! ruby generator/convert.rb &>/dev/null; then
    echo "[${MODULE}] ERROR: SVG generation failed (ruby generator/convert.rb)"
    exit 1
fi

SVG_SRC="$OREO_DIR/src/oreo_noctalia_${PRIMARY}_cursors"
if [ ! -d "$SVG_SRC" ]; then
    echo "[${MODULE}] ERROR: Expected SVG source dir not found: $SVG_SRC"
    exit 1
fi

echo "[${MODULE}] SVGs ready in $SVG_SRC"

# --- Step 3: Build XCursor (GTK fallback) ------------------------------------
echo "[${MODULE}] Building XCursor theme for GTK fallback (~18s)..."

BACKUP_DIR="/tmp/oreo-src-backup-$$"
mkdir -p "$BACKUP_DIR"
find "$OREO_DIR/src/" -mindepth 1 -maxdepth 1 -type d \
    ! -name "oreo_noctalia_${PRIMARY}_cursors" \
    -exec mv {} "$BACKUP_DIR/" \;

( cd "$OREO_DIR" && bash build.sh &>/dev/null )
BUILD_STATUS=$?

# Restore moved themes
mv "$BACKUP_DIR"/*/ "$OREO_DIR/src/" 2>/dev/null
rmdir "$BACKUP_DIR" 2>/dev/null

if [ $BUILD_STATUS -ne 0 ]; then
    echo "[${MODULE}] WARNING: XCursor build failed — GTK fallback unavailable"
else
    BUILT_XCURSOR="$OREO_DIR/dist/oreo_noctalia_${PRIMARY}_cursors"
    if [ -d "$BUILT_XCURSOR" ]; then
        cp -r "$BUILT_XCURSOR" "$XCURSOR_CACHE"
        echo "[${MODULE}] XCursor theme cached at $XCURSOR_CACHE"
    fi
fi

# --- Step 4: Build hyprcursor working-state from SVGs ------------------------
echo "[${MODULE}] Building hyprcursor working-state..."

WORK_DIR="/tmp/oreo_hypr_work_${PRIMARY}_$$"
HYPR_CURSORS_DIR="$WORK_DIR/hyprcursors"
mkdir -p "$WORK_DIR"

# Write manifest — name must match HYPR_THEME_NAME so compiled dir = HYPR_COMPILED_NAME
cat > "$WORK_DIR/manifest.hl" <<EOF
name = ${HYPR_THEME_NAME}
description = Oreo cursors in Noctalia primary colour #${PRIMARY}
version = 0.1
cursors_directory = hyprcursors
EOF

# For each SVG, create a shape directory with the SVG and a meta.hl
declare -A HOTSPOTS
HOTSPOTS["crosshair"]="0.5 0.5"
HOTSPOTS["cell"]="0.5 0.5"
HOTSPOTS["color-picker"]="0.0 1.0"
HOTSPOTS["zoom-in"]="0.5 0.5"
HOTSPOTS["zoom-out"]="0.5 0.5"
HOTSPOTS["all-scroll"]="0.5 0.5"
HOTSPOTS["fleur"]="0.5 0.5"
HOTSPOTS["move"]="0.5 0.5"

for svg in "$SVG_SRC"/*.svg; do
    shape=$(basename "$svg" .svg)
    shape_dir="$HYPR_CURSORS_DIR/$shape"
    mkdir -p "$shape_dir"
    cp "$svg" "$shape_dir/image.svg"

    hotspot="${HOTSPOTS[$shape]:-0.0 0.0}"
    hx=$(echo "$hotspot" | cut -d' ' -f1)
    hy=$(echo "$hotspot" | cut -d' ' -f2)

    cat > "$shape_dir/meta.hl" <<EOF
resize_algorithm = bilinear
hotspot_x = ${hx}
hotspot_y = ${hy}
define_size = 32, image.svg
EOF
done

echo "[${MODULE}] Working-state built with $(ls "$HYPR_CURSORS_DIR" | wc -l) shapes."

# --- Step 5: Compile with hyprcursor-util ------------------------------------
echo "[${MODULE}] Compiling hyprcursor theme..."

hyprcursor-util --create "$WORK_DIR" -o "$ICONS_DIR" &>/dev/null
HYPR_BUILD_STATUS=$?

rm -rf "$WORK_DIR"

if [ $HYPR_BUILD_STATUS -ne 0 ]; then
    echo "[${MODULE}] ERROR: hyprcursor-util --create failed"
    exit 1
fi

if [ ! -d "$HYPR_CACHE" ]; then
    echo "[${MODULE}] ERROR: Expected compiled theme not found at $HYPR_CACHE"
    echo "[${MODULE}]   Contents of $ICONS_DIR:"
    ls "$ICONS_DIR" | grep -i noctalia
    exit 1
fi

echo "[${MODULE}] Hyprcursor theme cached at $HYPR_CACHE"

# --- Step 6: Apply -----------------------------------------------------------
echo "[${MODULE}] Applying cursor theme..."
hyprctl setcursor "$HYPR_COMPILED_NAME" "$CURSOR_SIZE"
_apply_xcursor_fallback

echo "[${MODULE}] Cursors updated to #${PRIMARY} at size ${CURSOR_SIZE}."
